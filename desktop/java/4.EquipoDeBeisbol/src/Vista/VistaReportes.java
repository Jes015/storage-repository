    public VistaReportes(java.awt.Frame parent, Modelo.EquipoDeBeisbol equipo) {
        super(parent, true);
        initComponents();

        int numeroLanzadores9a12 = 0, numeroLanzadores13a16 = 0
                ,cantLanzadores = 0,cantReceptores = 0,cantPrimeraBase = 0,cantSegundoBase = 0, cantShortstop=0,cantJardinero = 0;
        
        float promLanzadores = 0,promReceptores = 0,promPrimeraBase = 0,promSegundoBase = 0, promShortstop=0,promJardinero = 0;

        Modelo.Nodo cabeza = equipo.getLista().getCabeza(), promedioBajo = cabeza;
        while (cabeza != null) {

            if (cabeza.getJugador().getEdad() <= 12 && cabeza.getJugador().getEdad() >= 9) {
                jPanel3.add(new panelJugador(cabeza.getJugador()));
                if (cabeza.getJugador().getPosicion() == Modelo.Posicion.LANZADOR) {
                    numeroLanzadores9a12++;
                }
            } else if (cabeza.getJugador().getEdad() <= 16 && cabeza.getJugador().getEdad() >= 13 && (cabeza.getJugador().getPosicion() == Modelo.Posicion.LANZADOR)) {
                numeroLanzadores13a16++;
            }

            if (cabeza.getJugador().getPromedioBateo() < promedioBajo.getJugador().getPromedioBateo()) {
                promedioBajo = cabeza;
            }
            
            if(null != cabeza.getJugador().getPosicion())switch (cabeza.getJugador().getPosicion()) {
                case JARDINERO:
                    promJardinero+=cabeza.getJugador().getPromedioBateo();
                    cantJardinero++;
                    break;
                case LANZADOR:
                    promLanzadores+=cabeza.getJugador().getPromedioBateo();
                    cantLanzadores++;
                    break;
                case PRIMERA_BASE:
                    promPrimeraBase+=cabeza.getJugador().getPromedioBateo();
                    cantPrimeraBase++;
                    break;
                case RECEPTOR:
                    promReceptores+=cabeza.getJugador().getPromedioBateo();
                    cantReceptores++;
                    break;
                case SEGUNDA_BASE:
                    promSegundoBase+=cabeza.getJugador().getPromedioBateo();
                    cantSegundoBase++;
                    break;
                case SHORTSTOP:
                    promShortstop+=cabeza.getJugador().getPromedioBateo();
                    cantShortstop++;
                    break;
                default:
                    break;
            }
            
            cabeza = cabeza.getSiguienteNodo();
        }
        
        this.jLabel16.setText(String.valueOf(promLanzadores/cantLanzadores));
        this.jLabel17.setText(String.valueOf(promReceptores/cantReceptores));
        this.jLabel18.setText(String.valueOf(promShortstop/cantShortstop));
        this.jLabel19.setText(String.valueOf(promJardinero/cantJardinero));
        this.jLabel20.setText(String.valueOf(promPrimeraBase/cantPrimeraBase));
        this.jLabel15.setText(String.valueOf(promSegundoBase/cantSegundoBase));
        

        jLabel5.setText(String.valueOf(numeroLanzadores9a12));
        jLabel7.setText(String.valueOf(numeroLanzadores13a16));

        jLabel4.setText(promedioBajo.getJugador().toString());

        for (int i = 0; i < 9; i++) {
            javax.swing.JPanel panel = new javax.swing.JPanel();
            panel.setBackground(new java.awt.Color(35, 35, 35));
            this.jPanel3.add(panel);
        }

        this.jPanel3.updateUI();
    }

