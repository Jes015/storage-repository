    public void actualizarTabla(Modelo.ListaEnlazadaSimple lista) {

        this.jPanel3.removeAll();

        Modelo.Nodo temporal = lista.getCabeza();

        while (temporal != null) {
            this.jPanel3.add(new panelJugador(temporal.getJugador()));
            temporal = temporal.getSiguienteNodo();
        }

        for (int i = 0; i < 15; i++) {
            javax.swing.JPanel panel = new javax.swing.JPanel();
            panel.setBackground(new java.awt.Color(35, 35, 35));
            this.jPanel3.add(panel);
        }

        this.jPanel3.updateUI();
    }

