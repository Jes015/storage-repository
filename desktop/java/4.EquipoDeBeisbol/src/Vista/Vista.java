    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (!(jTextField6.getText().trim().equalsIgnoreCase(""))) {
            Modelo.Nodo nodo = this.equipo.buscarJugador(jTextField6.getText().trim());

            if (nodo != null) {
                javax.swing.JOptionPane.showMessageDialog(this, nodo.getJugador().toString());
            } else {
                javax.swing.JOptionPane.showMessageDialog(this,"Error: jugador no encontrado");
            }
        }else{
            javax.swing.JOptionPane.showMessageDialog(this,"Error: debe escribir algo en el campo");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

