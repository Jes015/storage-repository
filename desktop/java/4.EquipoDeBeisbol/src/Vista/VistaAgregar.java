    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (jTextField1.getText().trim().equalsIgnoreCase("") && jTextField3.getText().trim().equalsIgnoreCase("") && jTextField4.getText().trim().equalsIgnoreCase("") && jTextField5.getText().trim().equalsIgnoreCase("")) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: debe llenar todos los campos");
        } else {
            if (this.jComboBox1.getSelectedIndex() == 1) {

                if (this.jComboBox4.getItemCount() != 0) {
                    Modelo.Nodo nodoTemporal = this.equipo.buscarJugador(this.jComboBox4.getSelectedItem().toString());
                    this.equipo.agregarJugador(nodoTemporal, new Modelo.Nodo(new Modelo.Jugador(jTextField3.getText(), jTextField1.getText(), Byte.valueOf(jTextField4.getText()), Modelo.Posicion.valueOf(jComboBox2.getSelectedItem().toString()), Float.valueOf(jTextField5.getText()))));
                } else {
                    javax.swing.JOptionPane.showMessageDialog(this, "Error: no se han agregado jugadores");
                }
            } else {
                String pos = (this.jComboBox1.getSelectedIndex() == 0) ? "inicio" : "final";
                if (this.equipo.agregarJugador(new Modelo.Nodo(new Modelo.Jugador(jTextField3.getText(), jTextField1.getText(), Byte.valueOf(jTextField4.getText()), Modelo.Posicion.valueOf(jComboBox2.getSelectedItem().toString()), Float.valueOf(jTextField5.getText()))), pos)) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Jugador agregado");
                } else {
                    javax.swing.JOptionPane.showMessageDialog(this, "Error: el numero de identificacion que desea agrega ya esta registrado en el sistema");
                }
            }

            this.parent.actualizarTabla(this.equipo.getLista());
            this.actulizarJCombox(jComboBox4);
            this.actulizarJCombox(jComboBox5);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

